package Basics;

public class Days {
	public static void main(String args[]) {
		String dayOfWeek = "Monday";
		switch (dayOfWeek) {
		case "Monday": 
			System.out.println("Day is " + dayOfWeek);
			break;
		case "Tuesday": 
			System.out.println("Day is " + dayOfWeek);
			break;
		case "Wednesday": 
			System.out.println("Day is " + dayOfWeek);
			break;
		case "Thurdsday": 
			System.out.println("Day is " + dayOfWeek);
			break;
		case "Friday": 
			System.out.println("Day is " + dayOfWeek);
			break;
		}
	}

}
